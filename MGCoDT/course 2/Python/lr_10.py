n = 0
p = 0
with open("text.txt") as file_handler:
    for line in file_handler:
        l = len(line) - 1
        print(line,'Кол-во символов: ', l)
        n += 1
        for i in line:
            if i == ' ':
                p += 1
        print('Количество слов: ',p + 1)
        p = 0
print('Всего строк: ', n)

