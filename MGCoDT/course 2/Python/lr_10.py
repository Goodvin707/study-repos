# Задание 1: Создать текстовый файл, записать в него построчно данные, которые вводит пользователь. Окончанием ввода пусть служит пустая строка.
t = open("text.txt", "w")
n = int(input('Введите количество строк: '))
for i in range(n):
    a = input('Введите текст: ')
    t.write(a)
    t.write("\n")
t.close()

